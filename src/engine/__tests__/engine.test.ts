// ---------- rng ----------

describe("rng", () => {
  it("is deterministic: same seed yields the same sequence", () => {
    const a = createRng("hoops-seed");
    const b = createRng("hoops-seed");
    for (let i = 0; i < 100; i++) expect(a()).toBe(b());
  });

  it("yields different sequences for different seeds", () => {
    const a = createRng("seed-one");
    const b = createRng("seed-two");
    const seqA = Array.from({ length: 10 }, () => a());
    const seqB = Array.from({ length: 10 }, () => b());
    expect(seqA).not.toEqual(seqB);
  });

  it("stays in [0,1) and helpers respect bounds", () => {
    const rng = createRng("bounds");
    for (let i = 0; i < 1000; i++) {
      const v = rng();
      expect(v).toBeGreaterThanOrEqual(0);
      expect(v).toBeLessThan(1);
    }
    const r2 = createRng("bounds2");
    for (let i = 0; i < 200; i++) {
      const n = randInt(r2, 3, 7);
      expect(n).toBeGreaterThanOrEqual(3);
      expect(n).toBeLessThanOrEqual(7);
    }
    const arr = [1, 2, 3, 4, 5];
    const shuffled = shuffle(createRng("s"), arr);
    expect(arr).toEqual([1, 2, 3, 4, 5]); // input untouched
    expect([...shuffled].sort()).toEqual([1, 2, 3, 4, 5]);
    expect(arr).toContain(pick(createRng("p"), arr));
  });
});


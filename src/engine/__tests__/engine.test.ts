function makePlayer(name: string, pos: Position, stats: PlayerStats): Player {
  return {
    id: `${name.toLowerCase().replace(/\s+/g, "-")}-test-1990s`,
    name,
    franchiseId: "test",
    decade: "1990s" as Decade,
    positions: [pos],
    stats,
    years: "1990–1999",
  };
}

function makeRoster(statsByPos: Record<Position, PlayerStats>): Roster {
  const roster = {} as Roster;
  for (const pos of POSITIONS) {
    roster[pos] = makePlayer(`Player ${pos}`, pos, statsByPos[pos]);
  }
  return roster;
}


// ---------- season ----------

describe("season", () => {
  it("is deterministic for the same roster + seed", () => {
    const a = simulateSeason(SUPERTEAM, "season-seed-42");
    const b = simulateSeason(SUPERTEAM, "season-seed-42");
    expect(a).toEqual(b);
    const c = simulateSeason(SUPERTEAM, "season-seed-43");
    expect(a.games.map((g) => g.win)).not.toEqual(c.games.map((g) => g.win));
  });

  it("produces 82 well-formed games", () => {
    const r = simulateSeason(SCRUB_TEAM, "shape-check");
    expect(r.games).toHaveLength(82);
    expect(r.wins + r.losses).toBe(82);
    expect(r.wins).toBe(r.games.filter((g) => g.win).length);
    for (const g of r.games) {
      if (g.win) expect(g.teamScore).toBeGreaterThan(g.oppScore);
      else expect(g.teamScore).toBeLessThan(g.oppScore);
    }
    const firstLossGame = r.games.find((g) => !g.win);
    expect(r.firstLoss).toBe(firstLossGame ? firstLossGame.gameNo : null);
  });

  it("calibration: superteam averages >= 75 wins and hits 82-0", () => {
    const seeds = 200;
    let totalWins = 0;
    let perfect = 0;
    for (let i = 0; i < seeds; i++) {
      const r = simulateSeason(SUPERTEAM, `calibration-super-${i}`);
      totalWins += r.wins;
      if (r.wins === 82) {
        perfect++;
        expect(r.firstLoss).toBeNull();
      }
    }
    const avg = totalWins / seeds;
    console.log(
      `[calibration] superteam avg wins=${avg.toFixed(2)}, 82-0 rate=${(
        (perfect / seeds) * 100
      ).toFixed(1)}% (overall=${teamStrength(SUPERTEAM).overall})`,
    );
    expect(avg).toBeGreaterThanOrEqual(75);
    expect(perfect).toBeGreaterThanOrEqual(1);
  });

  it("calibration: scrub team averages <= 55 wins", () => {
    const seeds = 200;
    let totalWins = 0;
    for (let i = 0; i < seeds; i++) {
      totalWins += simulateSeason(SCRUB_TEAM, `calibration-scrub-${i}`).wins;
    }
    const avg = totalWins / seeds;
    console.log(
      `[calibration] scrub avg wins=${avg.toFixed(2)} (overall=${
        teamStrength(SCRUB_TEAM).overall
      })`,
    );
    expect(avg).toBeLessThanOrEqual(55);
  });

  it("calibration: weak roster stays under 30 wins on average", () => {
    let totalWins = 0;
    for (let i = 0; i < 100; i++) {
      totalWins += simulateSeason(WEAK_TEAM, `calibration-weak-${i}`).wins;
    }
    const avg = totalWins / 100;
    console.log(
      `[calibration] weak avg wins=${avg.toFixed(2)} (overall=${
        teamStrength(WEAK_TEAM).overall
      })`,
    );
    expect(avg).toBeLessThan(30);
  });
});

// ---------- strength ----------

describe("strength", () => {
  it("handles null slots safely and keeps fields in range", () => {
    const empty: Roster = { PG: null, SG: null, SF: null, PF: null, C: null };
    const r = teamStrength(empty);
    expect(r.chemistry).toBe(0);
    expect(r.offense).toBe(95);
    expect(Number.isFinite(r.overall)).toBe(true);
    const full = teamStrength(SUPERTEAM);
    expect(full.chemistry).toBeGreaterThan(0);
    expect(full.chemistry).toBeLessThanOrEqual(1);
    expect(full.offense).toBeGreaterThanOrEqual(95);
    expect(full.offense).toBeLessThanOrEqual(135);
    expect(full.overall).toBeGreaterThan(teamStrength(SCRUB_TEAM).overall);
  });
});

// ---------- series ----------

describe("series", () => {
  it("is deterministic per seed", () => {
    const a = simulateSeries(SUPERTEAM, SCRUB_TEAM, "series-seed");
    const b = simulateSeries(SUPERTEAM, SCRUB_TEAM, "series-seed");
    expect(a).toEqual(b);
  });

  it("ends at 4 wins with plausible scores", () => {
    const r = simulateSeries(SCRUB_TEAM, SUPERTEAM, "series-shape");
    expect(Math.max(r.aWins, r.bWins)).toBe(4);
    expect(Math.min(r.aWins, r.bWins)).toBeLessThanOrEqual(3);
    expect(r.games.length).toBe(r.aWins + r.bWins);
    for (const g of r.games) {
      expect(g.aScore).not.toBe(g.bScore);
      expect(Math.min(g.aScore, g.bScore)).toBeGreaterThanOrEqual(90);
      expect(Math.max(g.aScore, g.bScore)).toBeLessThanOrEqual(135);
    }
    expect(r.winner).toBe(r.aWins > r.bWins ? "a" : "b");
  });

  it("a much stronger roster wins the series in >80% of seeds (both slots)", () => {
    let strongAsA = 0;
    let strongAsB = 0;
    for (let i = 0; i < 100; i++) {
      if (simulateSeries(SUPERTEAM, SCRUB_TEAM, `series-${i}`).winner === "a") {
        strongAsA++;
      }
      if (simulateSeries(SCRUB_TEAM, SUPERTEAM, `series-${i}`).winner === "b") {
        strongAsB++;
      }
    }
    console.log(
      `[calibration] strong roster series win rate: slot a=${strongAsA}%, slot b=${strongAsB}%`,
    );
    expect(strongAsA).toBeGreaterThan(80);
    expect(strongAsB).toBeGreaterThan(80);
  });
});


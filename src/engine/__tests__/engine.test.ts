describe("shareText", () => {
  it("contains the header, record, and 82 squares", () => {
    const result = simulateSeason(SUPERTEAM, "share-seed");
    const text = shareText(result, { mode: "daily", dateKey: "2026-06-10" });
    expect(text).toContain("🏀 Hoops 82–0 · Daily 2026-06-10");
    expect(text).toContain(`Record: ${result.wins}–${result.losses}`);
    const squares = (text.match(/🟩|🟥/g) ?? []).length;
    expect(squares).toBe(82);
    expect((text.match(/🟩/g) ?? []).length).toBe(result.wins);
    expect(text).toContain("https://krhamby.github.io/Hoops/");
    // rows of 14: 5 full rows + one row of 12
    const rows = text.split("\n").filter((l) => /^(🟩|🟥)+$/.test(l));
    expect(rows).toHaveLength(6);
    expect(rows.slice(0, 5).every((row) => (row.match(/🟩|🟥/g) ?? []).length === 14)).toBe(true);

    expect(shareText(result, { mode: "free" })).toContain("Free Play");
    expect(shareText(result, { mode: "versus" })).toContain("Versus");
  });
});
